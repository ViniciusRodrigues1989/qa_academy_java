package exemplosMetodos;

public class ExemploMetodoSemRetorno {

    public static void metodoum(){
        System.out.println("Teste metodo sem retorno um");
    }
    public static void metododois(){
        System.out.println("Teste metodo sem retorno dois");
    }
    public static void metodotres(){
        System.out.println("Teste metodo sem retorno tres");
    }
    public static void metodoquatro(){
        System.out.println("Teste metodo sem retorno quatro");
    }
    public static void metodocinco(){
        System.out.println("Teste metodo sem retorno cinco");
    }
    public static void main(String[] args){
        metodoum();
        metododois();
        metodotres();
        metodoquatro();
        metodocinco();
    }
}
